package Java12;

public class Member {
    private String name;
    private int age;
    private int id;

    public Member(String name, int age, int id){
        this.name=name;
        this.age=age;
        this.age=age;
    }
    public String getName(){
        return name;
    }
    public int getAge(){
        return age;
    }
    public int getId(){
        return id;
    }
}
